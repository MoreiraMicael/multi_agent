class DataProcessor:
    """
    A generic data processing class demonstrating best practices.
    """
    def __init__(self: 'DataProcessor', name: str = "Default Processor") -> None:
        """
        Initialize the DataProcessor.
        Args:
            name (str): The name of the processor.
        """
        self.name: str = name
        self.processed_items: list = []

    def process_data(self: 'DataProcessor', data: list) -> list:
        """
        Process a list of data items.
        Args:
            data (list): The data to process.
        Returns:
            list: The processed data.
        """
        if not isinstance(data, list):
            raise TypeError("Data must be a list")
        processed: list = []
        for item in data:
            try:
                processed_item = self._process_item(item)
                processed.append(processed_item)
                self.processed_items.append(processed_item)
            except Exception as e:
                print(f"Error processing item {item}: {e}")
                continue
        return processed

    def _process_item(self: 'DataProcessor', item: object) -> str:
        """
        Process a single item (override in subclasses).
        Args:
            item (object): The item to process.
        Returns:
            str: The processed item as a string in uppercase.
        """
        return str(item).upper()

    def get_statistics(self: 'DataProcessor') -> dict:
        """
        Get processing statistics.
        Returns:
            dict: Statistics about processed items.
        """
        return {
            'processor_name': self.name,
            'total_processed': len(self.processed_items),
            'last_processed': self.processed_items[-1] if self.processed_items else None
        }


