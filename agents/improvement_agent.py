def analyze_python_code(code: str) -> Tuple[list, list, list, list]:
    """
    Analyze Python code for code quality, docstring, type hints, and design.
    Returns four lists of suggestions.
    """
    quality, docstring, typehints, design = [], [], [], []
    try:
        tree = ast.parse(code)
        for node in ast.walk(tree):
            if isinstance(node, ast.FunctionDef):
                # Docstring check
                if ast.get_docstring(node) is None:
                    docstring.append(
                        f"Function '{node.name}' is missing a docstring."
                    )
                # Type hints check
                if not node.returns:
                    typehints.append(
                        f"Function '{node.name}' is missing a return type hint."
                    )
                for arg in node.args.args:
                    if not arg.annotation:
                        typehints.append(
                            f"Argument '{arg.arg}' in function '{node.name}' is missing a type hint."
                        )
            if isinstance(node, ast.ClassDef):
                # Docstring check
                if ast.get_docstring(node) is None:
                    docstring.append(
                        f"Class '{node.name}' is missing a docstring."
                    )
                # Design: check for too many methods (arbitrary threshold)
                method_count = sum(isinstance(n, ast.FunctionDef) for n in node.body)
                if method_count > 10:
                    design.append(
                        f"Class '{node.name}' has too many methods ({method_count}). Consider refactoring."
                    )
        # Code quality: check for long lines
        for i, line in enumerate(code.splitlines(), 1):
            if len(line) > 100:
                quality.append(
                    f"Line {i} exceeds 100 characters."
                )
    except Exception as e:
        quality.append(f"Error analyzing code: {e}")
    return quality, docstring, typehints, design

